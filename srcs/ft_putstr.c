#include "../includes/fillit.h"

void	ft_putstr(char const *s)
{
	unsigned	i;

	i = 0;
	if (s == NULL)
		return ;
	while (s[i] != '\0')
	{
		ft_putchar(s[i]);
		i++;
	}
}
